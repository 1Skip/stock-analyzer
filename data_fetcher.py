"""
股票数据获取模块 - 获取真实股票数据
支持A股、港股、美股
针对Streamlit Cloud优化版本（使用yfinance作为主要数据源）
"""
import yfinance as yf
import pandas as pd
from datetime import datetime, timedelta
import time
import random


class StockDataFetcher:
    """股票数据获取器 - 带重试机制"""

    def __init__(self):
        self.cache = {}
        self.use_yfinance_for_cn = True
        self.max_retries = 3  # 最大重试次数
        self.retry_delay = 1  # 重试间隔（秒）

    def _retry_with_backoff(self, func, *args, **kwargs):
        """带指数退避的重试机制"""
        for attempt in range(self.max_retries):
            try:
                result = func(*args, **kwargs)
                if result is not None and (not isinstance(result, pd.DataFrame) or not result.empty):
                    return result
            except Exception as e:
                print(f"尝试 {attempt + 1}/{self.max_retries} 失败: {str(e)}")

            if attempt < self.max_retries - 1:
                # 指数退避 + 随机抖动
                delay = self.retry_delay * (2 ** attempt) + random.uniform(0, 0.5)
                time.sleep(delay)

        return None

    def get_stock_data(self, symbol, period="1y", interval="1d", market="US"):
        """
        获取股票历史数据 - 带重试机制
        """
        cache_key = f"{symbol}_{period}_{interval}_{market}"

        if cache_key in self.cache:
            cache_time, cache_data = self.cache[cache_key]
            if datetime.now() - cache_time < timedelta(minutes=5):
                return cache_data

        def _fetch_data():
            if market == "CN":
                return self._get_cn_stock_data_yfinance(symbol, period)
            elif market == "HK":
                ticker = yf.Ticker(f"{symbol}.HK")
                return ticker.history(period=period, interval=interval)
            else:
                ticker = yf.Ticker(symbol)
                return ticker.history(period=period, interval=interval)

        data = self._retry_with_backoff(_fetch_data)

        if data is None or (isinstance(data, pd.DataFrame) and data.empty):
            print(f"未找到股票 {symbol} 的数据（已重试{self.max_retries}次）")
            return None

        # 标准化列名
        data.columns = [col.lower().replace(' ', '_') for col in data.columns]

        # 缓存数据
        self.cache[cache_key] = (datetime.now(), data)

        return data

    def _get_cn_stock_data_yfinance(self, symbol, period):
        """使用yfinance获取A股数据（在境外服务器更快）"""
        # A股代码加后缀
        if '.' not in symbol:
            # 尝试上海交易所
            symbol_yf = f"{symbol}.SS"
            ticker = yf.Ticker(symbol_yf)
            data = ticker.history(period=period)

            if data.empty:
                # 尝试深圳交易所
                symbol_yf = f"{symbol}.SZ"
                ticker = yf.Ticker(symbol_yf)
                data = ticker.history(period=period)
        else:
            ticker = yf.Ticker(symbol)
            data = ticker.history(period=period)

        return data

    def get_stock_info(self, symbol, market="US"):
        """获取股票基本信息 - 带重试机制"""
        def _fetch_info():
            if market == "HK":
                ticker = yf.Ticker(f"{symbol}.HK")
                return ticker.info
            elif market == "CN":
                # A股使用yfinance
                if '.' not in symbol:
                    # 先尝试上海交易所
                    ticker = yf.Ticker(f"{symbol}.SS")
                    info = ticker.info
                    if info and info.get('shortName'):
                        return info
                    # 再尝试深圳交易所
                    ticker = yf.Ticker(f"{symbol}.SZ")
                    return ticker.info
                else:
                    ticker = yf.Ticker(symbol)
                    return ticker.info
            else:
                ticker = yf.Ticker(symbol)
                return ticker.info

        info = self._retry_with_backoff(_fetch_info)

        if info is None:
            print(f"获取股票信息失败 {symbol}（已重试{self.max_retries}次）")
            return {}

        return info

    def get_stock_name(self, symbol, market="US"):
        """获取股票名称，优先使用映射表，其次从yfinance获取"""
        # A股优先使用本地映射表（最可靠）
        if market == "CN":
            name = CN_STOCK_NAMES_EXTENDED.get(symbol)
            if name:
                return name

        # 尝试从yfinance获取
        try:
            info = self.get_stock_info(symbol, market)
            if info:
                # 尝试多个可能的字段
                name = info.get('shortName') or info.get('longName')
                if name:
                    return name
        except Exception as e:
            print(f"从yfinance获取名称失败 {symbol}: {e}")

        # 如果都失败了，返回代码本身
        return symbol

    def get_realtime_quote(self, symbol, market="US"):
        """获取实时行情"""
        try:
            if market == "HK":
                ticker = yf.Ticker(f"{symbol}.HK")
            elif market == "CN":
                # A股使用yfinance
                if '.' not in symbol:
                    ticker = yf.Ticker(f"{symbol}.SS")
                else:
                    ticker = yf.Ticker(symbol)
            else:
                ticker = yf.Ticker(symbol)

            hist = ticker.history(period="5d")
            info = ticker.info

            if not hist.empty:
                latest = hist.iloc[-1]
                prev = hist.iloc[-2] if len(hist) > 1 else latest

                return {
                    'symbol': symbol,
                    'name': info.get('shortName', symbol),
                    'price': latest['Close'],
                    'open': latest['Open'],
                    'high': latest['High'],
                    'low': latest['Low'],
                    'volume': latest['Volume'],
                    'prev_close': prev['Close'],
                    'change': ((latest['Close'] - prev['Close']) / prev['Close'] * 100)
                }

        except Exception as e:
            print(f"获取实时行情失败 {symbol}: {str(e)}")

        return None


